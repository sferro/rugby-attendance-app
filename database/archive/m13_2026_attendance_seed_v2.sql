INSERT INTO attendance_records (
    player_team_id,
    training_session_id,
    attendance_status_id,
    created_by,
    updated_by
)
SELECT
    pt.id AS player_team_id,
    ts.id AS training_session_id,
    ast.id AS attendance_status_id,
    'seed' AS created_by,
    'seed' AS updated_by
FROM temp_m13_attendance tmp
JOIN players p
    ON p.first_name = tmp.first_name
   AND p.last_name = tmp.last_name
JOIN player_teams pt
    ON pt.player_id = p.id
JOIN teams t
    ON t.id = pt.team_id
   AND t.name = 'M13'
JOIN seasons s
    ON s.id = t.season_id
   AND s.year = 2026
JOIN training_session_types tst
    ON tst.code = 'training'
JOIN training_sessions ts
    ON ts.team_id = t.id
   AND ts.training_session_type_id = tst.id
   AND ts.session_date = tmp.session_date
JOIN attendance_statuses ast
    ON ast.code = tmp.attendance_status_code
ON CONFLICT (player_team_id, training_session_id)
DO UPDATE SET
    attendance_status_id = EXCLUDED.attendance_status_id,
    updated_by = EXCLUDED.updated_by,
    updated_at = CURRENT_TIMESTAMP;

